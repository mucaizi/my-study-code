package com.study.code;

public class CharOperationDemo {
    public static void main() {
        char x = 'a';
        char y = (char) (x  +  1);
        int i = y + 1;
        System.out.println(y + " " + i );//b 99

        boolean l = true;
        System.out.println(l + "1");//true1

        System.out.println(1 + 2 + " " + 1  + 2);// 3 12
    }
}
