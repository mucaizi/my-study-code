package com.study.code;

public class ArithmeticOperatorsDemo1 {
    public static void main(String[]args){

        //4
        System.out.println(3+1);
        //2
        System.out.println(3-1);
        //6
        System.out.println(3*2);
        //2
        System.out.println(4/2);
        //1
        System.out.println(3%2);
        //0.30000000000000004
        System.out.println(0.1+0.2);
    }
}
